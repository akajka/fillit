char	*ft_strcat(char *dest, const char *app)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (app[j] != '\0')
	{
		dest[i + j] = app[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}
